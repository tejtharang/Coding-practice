package com.tej.DesignPatterns;


/*
    Utility or Helper classes are fitted with static methods that can be called to enable code reuse.
    These classes can be used when no other classes can accommodate these methods.

    These classes have a private constructor that cannot be called
 */
public class UtilityHelper {

    private UtilityHelper(){
        //empty
    }

    public static void helperMethod1(){
        //empty for now
    }

    public static void helperMethod2(){
        //empty for now
    }
}
